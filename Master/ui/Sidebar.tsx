export function Sidebar({ user, isOpen, onToggle }: SidebarProps) {
  const [isMobile, setIsMobile] = useState(false)
  const [pageRules, setPageRules] = useState<PageAccessRules | null>(null)
  const pathname = usePathname()

  useEffect(() => {
    const checkMobile = () => {
      const mobile = window.innerWidth < 1024
      setIsMobile(mobile)
    }
    checkMobile()
    window.addEventListener('resize', checkMobile)
    return () => window.removeEventListener('resize', checkMobile)
  }, [])

  useEffect(() => {
    if (isMobile && isOpen) {
      document.body.style.overflow = 'hidden'
    } else {
      document.body.style.overflow = 'unset'
    }
    return () => {
      document.body.style.overflow = 'unset'
    }
  }, [isMobile, isOpen])

  useEffect(() => {
    let cancelled = false
    fetch('/api/access/page-rules', { credentials: 'include' })
      .then((res) => (res.ok ? res.json() : null))
      .then((data) => {
        if (!cancelled && data?.rules) setPageRules(data.rules)
      })
      .catch(() => {})
    return () => {
      cancelled = true
    }
  }, [])

  const filteredNavigation = navigation.filter((item) => {
    const role = user?.role ?? ''
    if (pageRules) {
      return canAccessPath(item.href, role, pageRules)
    }
    if (!item.roles) return true
    return item.roles.includes(role)
  })

  return (
    <>
      {/* Mobile overlay - behind sidebar, below navbar */}
      {isMobile && isOpen && (
        <div
          className="fixed top-16 left-0 right-0 bottom-0 bg-opacity-50 z-40 lg:hidden"
          onClick={onToggle}
          aria-hidden="true"
        />
      )}

      {/* Sidebar - above overlay */}
      <aside
        className={`
          fixed top-16 left-0 z-50 h-[calc(100vh-4rem)] bg-[#1F3A5F] text-[#CBD5F5]
          transition-all duration-300 ease-in-out
          ${isOpen ? 'w-64' : 'w-20'}
          ${isMobile && !isOpen ? '-translate-x-full' : 'translate-x-0'}
          shadow-lg border-r border-[#152A4A]
          flex flex-col
          lg:z-30
        `}
      >

        {/* Navigation */}
        <nav className="flex-1 px-3 py-4 space-y-1 overflow-y-auto">
          {filteredNavigation.map((item) => {
            const isActive = pathname === item.href || pathname.startsWith(item.href + '/')
            return (
              <Link
                key={item.name}
                href={item.href}
                className={`
                  flex items-center gap-3 px-3 py-2.5 rounded-lg transition-all duration-200 group
                  ${isActive
                    ? 'bg-[rgba(244,180,0,0.15)] text-[#F4B400] border-l-4 border-[#F4B400] font-semibold'
                    : 'text-[#CBD5F5] hover:bg-[rgba(244,180,0,0.1)] hover:text-[#F4B400]'
                  }
                  ${!isOpen ? 'justify-center' : ''}
                `}
                title={!isOpen ? item.name : undefined}
              >
                <span className={`shrink-0 ${isActive ? 'text-[#F4B400]' : 'text-[#CBD5F5] group-hover:text-[#F4B400]'}`}>
                  {item.icon}
                </span>
                {isOpen && (
                  <span className="whitespace-nowrap">{item.name}</span>
                )}
              </Link>
            )
          })}
        </nav>

      </aside>
    </>
  )
}

